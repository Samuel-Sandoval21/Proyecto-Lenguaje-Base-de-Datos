CREATE TABLE CATEGORIAS (
    ID_CATEGORIA NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    NOMBRE_CATEGORIA VARCHAR2(100) NOT NULL,
    TIPO_GENERAL VARCHAR2(50) NOT NULL
);

CREATE TABLE MARCAS (
    ID_MARCA NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    NOMBRE_MARCA VARCHAR2(100) NOT NULL UNIQUE
);

CREATE TABLE METODOS_PAGO (
    ID_METODO NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR2(50) NOT NULL UNIQUE
);

CREATE TABLE CLIENTES (
    ID_CLIENTE NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR2(100) NOT NULL,
    APELLIDO VARCHAR2(100) NOT NULL,
    FECHA_REGISTRO DATE DEFAULT SYSDATE
);

CREATE TABLE PRODUCTOS (
    ID_PRODUCTO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_CATEGORIA NUMBER NOT NULL,
    ID_MARCA NUMBER NOT NULL,
    NOMBRE VARCHAR2(100) NOT NULL,
    DESCRIPCION VARCHAR2(200),
    PRECIO NUMBER(10,2) NOT NULL CHECK (PRECIO > 0),
    STOCK NUMBER DEFAULT 0 NOT NULL CHECK (STOCK >= 0),
    IMAGEN VARCHAR2(1000),
    CONSTRAINT UK_PRODUCTO UNIQUE (NOMBRE, ID_MARCA),
    CONSTRAINT FK_PRODUCTO_CATEGORIA
        FOREIGN KEY (ID_CATEGORIA)
        REFERENCES CATEGORIAS(ID_CATEGORIA),
    CONSTRAINT FK_PRODUCTO_MARCA
        FOREIGN KEY (ID_MARCA)
        REFERENCES MARCAS(ID_MARCA)
);


CREATE TABLE PRODUCTO_ATRIBUTOS (
    ID_ATRIBUTO NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    ID_PRODUCTO NUMBER NOT NULL,
    NOMBRE_ATRIBUTO VARCHAR2(100) NOT NULL,
    VALOR VARCHAR2(200) NOT NULL,
    CONSTRAINT FK_ATRIBUTO_PRODUCTO
        FOREIGN KEY (ID_PRODUCTO)
        REFERENCES PRODUCTOS(ID_PRODUCTO)
        ON DELETE CASCADE
);


CREATE TABLE VENTAS (
    ID_VENTA NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    ID_CLIENTE NUMBER NOT NULL,
    ID_METODO NUMBER NOT NULL,
    FECHA_VENTA DATE DEFAULT SYSDATE,
    TOTAL NUMBER(10,2) NOT NULL CHECK (TOTAL >= 0),
    CONSTRAINT FK_VENTA_CLIENTE
        FOREIGN KEY (ID_CLIENTE)
        REFERENCES CLIENTES(ID_CLIENTE),
    CONSTRAINT FK_VENTA_METODO
        FOREIGN KEY (ID_METODO)
        REFERENCES METODOS_PAGO(ID_METODO)
);

CREATE TABLE DETALLE_VENTA (
    ID_DETALLE NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    ID_VENTA NUMBER NOT NULL,
    ID_PRODUCTO NUMBER NOT NULL,
    CANTIDAD NUMBER NOT NULL CHECK (CANTIDAD > 0),
    PRECIO_UNITARIO NUMBER(10,2) NOT NULL CHECK (PRECIO_UNITARIO > 0),
    CONSTRAINT FK_DETALLE_VENTA
        FOREIGN KEY (ID_VENTA)
        REFERENCES VENTAS(ID_VENTA)
        ON DELETE CASCADE,
    CONSTRAINT FK_DETALLE_PRODUCTO
        FOREIGN KEY (ID_PRODUCTO)
        REFERENCES PRODUCTOS(ID_PRODUCTO)
);


CREATE TABLE USUARIOS (
    ID_USUARIO NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    USERNAME VARCHAR2(50) UNIQUE NOT NULL,
    PASSWORD VARCHAR2(200) NOT NULL,
    ROL VARCHAR2(20) CHECK (ROL IN ('ADMIN','VENDEDOR','CLIENTE')) NOT NULL,
    ID_CLIENTE NUMBER,
    CONSTRAINT FK_USUARIO_CLIENTE
        FOREIGN KEY (ID_CLIENTE)
        REFERENCES CLIENTES(ID_CLIENTE)
);

CREATE TABLE PROVEEDORES (
    ID_PROVEEDOR NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR2(150) NOT NULL,
    CORREO VARCHAR2(150) UNIQUE CHECK (CORREO LIKE '%@%')
);

CREATE TABLE TELEFONOS (
    ID_TELEFONO NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    ID_CLIENTE NUMBER NOT NULL,
    TELEFONO VARCHAR2(8) NOT NULL UNIQUE,
    CONSTRAINT FK_TELEFONOS_CLIENTE
        FOREIGN KEY (ID_CLIENTE)
        REFERENCES CLIENTES(ID_CLIENTE)
        ON DELETE CASCADE
);

CREATE TABLE TELEFONOS_PROVEEDOR (
    ID_TELEFONO NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    ID_PROVEEDOR NUMBER NOT NULL,
    TELEFONO VARCHAR2(20) NOT NULL UNIQUE,
    CONSTRAINT FK_TEL_PROVEEDOR
        FOREIGN KEY (ID_PROVEEDOR)
        REFERENCES PROVEEDORES(ID_PROVEEDOR)
        ON DELETE CASCADE
);


CREATE TABLE CORREOS (
    ID_CORREO NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    ID_CLIENTE NUMBER NOT NULL,
    CORREO VARCHAR2(150) UNIQUE CHECK (CORREO LIKE '%@%'),
    CONSTRAINT FK_CORREOS_CLIENTE
        FOREIGN KEY (ID_CLIENTE)
        REFERENCES CLIENTES(ID_CLIENTE)
        ON DELETE CASCADE
);

CREATE TABLE DIRECCIONES (
    ID_DIRECCION NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    ID_CLIENTE NUMBER NOT NULL,
    DIRECCION VARCHAR2(250) NOT NULL,
    CONSTRAINT FK_DIRECCIONES_CLIENTE
        FOREIGN KEY (ID_CLIENTE)
        REFERENCES CLIENTES(ID_CLIENTE)
        ON DELETE CASCADE     
);

CREATE TABLE AUDITORIA_SISTEMA (
    ID_AUDITORIA NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    TABLA_AFECTADA VARCHAR2(50),
    OPERACION VARCHAR2(10),
    ID_REGISTRO NUMBER,
    CAMPO_MODIFICADO VARCHAR2(50),
    VALOR_ANTERIOR VARCHAR2(200),
    VALOR_NUEVO VARCHAR2(200),
    FECHA_CAMBIO DATE DEFAULT SYSDATE,
    USUARIO_DB VARCHAR2(50)
);

